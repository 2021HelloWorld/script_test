#!/usr/bin/env bash
# config.sh —— EASIM 发布/回退方案统一配置
#
# 这是唯一存放机器相关路径的地方。脚本逻辑不硬编码任何路径。
# 解析优先级（高 -> 低）：命令行参数 > 环境变量 > 本文件默认值。
#
# 用法：被各脚本 `source` 引入；可直接编辑本文件改默认值，
#       也可在运行时用环境变量覆盖，例如：
#           STABLE_ROOT=/mnt/big/easim_stable ./create_stable.sh 2.0.1
#
# 注意：本文件只做“赋默认值”，不做校验与副作用，校验在 lib.sh 中完成。

# ---------------------------------------------------------------------------
# CODE_DIR：测试机 code 目录（easim 仓库根）。
#   优先使用显式设置（可通过 release_wizard 菜单 7 修改），
#   显式设置为空时自动探测当前 git 仓库根。
#   探测失败（不在 git 仓库内）时回退为空，由 lib.sh 报错提示。
# ---------------------------------------------------------------------------
: "${CODE_DIR:=/home/sensetime/Sensetime_ACE/kongxiaoqiang/easim/easim}"
if [[ -z "${CODE_DIR:-}" ]]; then
  CODE_DIR="$(git -C "$(pwd)" rev-parse --show-toplevel 2>/dev/null || true)"
fi

# ---------------------------------------------------------------------------
# STABLE_ROOT：测试机上的稳定版本备份库。单版本约 12G，确保磁盘充足。
# ---------------------------------------------------------------------------
: "${STABLE_ROOT:=/home/sensetime/Sensetime_ACE/kongxiaoqiang/easim/easim_stable}"

# ---------------------------------------------------------------------------
# PROD_CODE：生产机上的 code 运行目录（git checkout + 资产同步的目标）。
#   作为默认值使用：prod_hosts.txt 未给出第四列 code 路径的机器，
#   将回退到此处。各机器路径不一致时，在 prod_hosts.txt 第四列单独指定。
# ---------------------------------------------------------------------------
: "${PROD_CODE:=/home/sensetime/Test_env/easim}"

# ---------------------------------------------------------------------------
# PROD_HOSTS_FILE：生产机清单文件。每行 “机器名 IP SSH用户 [code路径]”，
#   第四列可选，# 开头为注释。
#   默认取 STABLE_ROOT/prod_hosts.txt。
# ---------------------------------------------------------------------------
: "${PROD_HOSTS_FILE:=${STABLE_ROOT}/prod_hosts.txt}"

# ---------------------------------------------------------------------------
# GIT_REMOTE：生产机 fetch 代码用的远程名。
# ---------------------------------------------------------------------------
: "${GIT_REMOTE:=origin}"

# ---------------------------------------------------------------------------
# ASSET_PATHS：纳入快照的资产相对路径列表（相对 CODE_DIR）。
#   当前只有一项；未来新增/改名只改这里。显式列出而非整目录同步，
#   是为了避免把同级的 _bak_ 历史备份误卷入快照。
#   可用环境变量 ASSET_PATHS_OVERRIDE（空格分隔）覆盖。
# ---------------------------------------------------------------------------
if [[ -n "${ASSET_PATHS_OVERRIDE:-}" ]]; then
  # shellcheck disable=SC2206
  ASSET_PATHS=( ${ASSET_PATHS_OVERRIDE} )
else
  ASSET_PATHS=(
    # 未来新增/改名直接增改本数组，例如：
    # "assets/environment/Office_11F_Room02"
    "assets/environment/Office_10F_Room01"
  )
fi

# ---------------------------------------------------------------------------
# RSYNC_EXCLUDES：快照与同步时排除的无关内容（相对每个资产目录）。
#   .git/.agents/.codex 是资产目录内的无关隐藏目录，不应进入快照。
# ---------------------------------------------------------------------------
RSYNC_EXCLUDES=(
  ".git"
  ".agents"
  ".codex"
)

# ---------------------------------------------------------------------------
# SSH_OPTS：SSH 统一选项。BatchMode=yes 保证免密未配好时快速失败而非卡住。
# ---------------------------------------------------------------------------
: "${SSH_CONNECT_TIMEOUT:=10}"
SSH_OPTS=(
  -o BatchMode=yes
  -o "ConnectTimeout=${SSH_CONNECT_TIMEOUT}"
  -o StrictHostKeyChecking=accept-new
)

# ---------------------------------------------------------------------------
# HEALTHCHECK_ENABLED：健康检查开关（功能待定，默认关闭）。
#   置 1 后调用 healthcheck_remote()（见 lib.sh，目前为占位钩子）。
# ---------------------------------------------------------------------------
: "${HEALTHCHECK_ENABLED:=0}"
